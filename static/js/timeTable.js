const today = new Date();
const options = {
  weekday: "long",
  year: "numeric",
  month: "long",
  day: "numeric",
};

// دالة لتنسيق التاريخ
function formatDate(date) {
  return date.toLocaleDateString("en-US", options);
}

// تعيين التاريخ الحالي للجدولين
document.getElementById("todayDate").textContent = `${formatDate(today)}`;

document.getElementById("prevDay").addEventListener("click", function () {
  today.setDate(today.getDate() - 1); // الانتقال إلى اليوم السابق
  document.getElementById("todayDate").textContent = `${formatDate(today)}`;
  updateTable(); // تحديث الجدول بناءً على التاريخ الجديد
});

document.getElementById("nextDay").addEventListener("click", function () {
  today.setDate(today.getDate() + 1); // الانتقال إلى اليوم التالي
  document.getElementById("todayDate").textContent = `${formatDate(today)}`;
  updateTable(); // تحديث الجدول بناءً على التاريخ الجديد
});

// دالة لتحديث محتويات الجدول بناءً على اليوم
function updateTable() {
  // هنا يمكنك تحديث الجدول استنادًا إلى اليوم
  // على سبيل المثال، إذا كان اليوم "الإثنين" سيتم ملء الجدول مع مواعيد الاثنين
  let timetable;
  switch (today.getDay()) {
    case 0:
      timetable = Sunday;
      break;
    case 1:
      timetable = Monday;
      break;
    case 2:
      timetable = Tuesday;
      break;
    case 3:
      timetable = Wednesday;
      break;
    case 4:
      timetable = Thursday;
      break;
    case 5:
      timetable = Friday;
      break;
    case 6:
      timetable = Saturday;
      break;
  }

  // تحديث الجدول باستخدام بيانات اليوم المحدد
  const tbody = document.querySelector("#timetable tbody");
  tbody.innerHTML = ""; // مسح البيانات السابقة
  timetable.forEach((item) => {
    const tr = document.createElement("tr");
    tr.innerHTML = `
      <td>${item.time}</td>
      <td>${item.roomNumber}</td>
      <td>${item.subject}</td>
      <td>${item.type}</td>
    `;
    tbody.appendChild(tr);
  });
}

